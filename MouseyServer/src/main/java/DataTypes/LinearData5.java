package DataTypes;

public class LinearData5 {
    private double y;
    private double x1;
    private double x2;
    private double x3;
    private double x4;
    private double x5;

    public LinearData5(double y, double x1, double x2, double x3, double x4, double x5) {
        this.y=y;
        this.x1=x1;
        this.x2=x2;
        this.x3=x3;
        this.x4=x4;
        this.x5=x5;
    }

    //Getters
    public double getY() {
        return y;
    }

    public double getX1() {
        return x1;
    }

    public double getX2() {
        return x2;
    }

    public double getX3() {
        return x3;
    }

    public double getX4() {
        return x4;
    }

    public double getX5() {
        return x5;
    }
}
